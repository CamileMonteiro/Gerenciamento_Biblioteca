    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tabelaLivros.getSelectedRow();
        if (linha != -1) {

            modoEdicao = true; // entrou em modo edi√ß√£o
            btnCadastrar.setEnabled(false);

            txtID.setText(tabelaLivros.getValueAt(linha, 0).toString());
            txtTitulo.setText(tabelaLivros.getValueAt(linha, 1).toString());
            txtAutor.setText(tabelaLivros.getValueAt(linha, 2).toString());
            txtAno.setText(tabelaLivros.getValueAt(linha, 3).toString());
            txtNota.setText(tabelaLivros.getValueAt(linha, 4).toString());
            cmbResenha.setSelectedItem(tabelaLivros.getValueAt(linha, 5).toString());

            txtID.setEditable(false);

        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela para editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        try {
            Livro l = new Livro();
            l.setId(Integer.parseInt(txtID.getText()));
            l.setTitulo(txtTitulo.getText());
            l.setAutor(txtAutor.getText());
            l.setAnoLeitura(Integer.parseInt(txtAno.getText()));
            l.setNota(Integer.parseInt(txtNota.getText()));
            l.setResenha(cmbResenha.getSelectedItem().toString());

            dao.atualizar(l);

            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");

            modoEdicao = false;
            btnCadastrar.setEnabled(true);
            limparCampos();
            preencherTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabelaLivros.getSelectedRow();
        if (linha != -1) {
            int id = Integer.parseInt(tabelaLivros.getValueAt(linha, 0).toString());

            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Tem certeza que deseja excluir o registro ID " + id + "?",
                    "Confirmar Exclus√£o",
                    JOptionPane.YES_NO_OPTION
            );
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    dao.excluir(id);
                    preencherTabela();
                    JOptionPane.showMessageDialog(this, "Exclu√≠do!");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (modoEdicao) {
            JOptionPane.showMessageDialog(this,
                    "Voc√™ est√° no modo de edi√ß√£o. Use o bot√£o ATUALIZAR.");
            return;
        }

        if (txtID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um n√∫mero para o ID!");
            txtID.requestFocus();
            return;
        }

        try {
            Livro l = new Livro();

            l.setId(Integer.parseInt(txtID.getText()));

            l.setTitulo(txtTitulo.getText());
            l.setAutor(txtAutor.getText());
            l.setAnoLeitura(Integer.parseInt(txtAno.getText()));
            l.setNota(Integer.parseInt(txtNota.getText()));

            if (cmbResenha.getSelectedItem() != null) {
                l.setResenha(cmbResenha.getSelectedItem().toString());
            } else {
                l.setResenha("");
            }

            dao.salvar(l);

            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
            limparCampos();
            preencherTabela();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O ID deve conter apenas n√∫meros!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar (verifique se o ID j√° existe): " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

