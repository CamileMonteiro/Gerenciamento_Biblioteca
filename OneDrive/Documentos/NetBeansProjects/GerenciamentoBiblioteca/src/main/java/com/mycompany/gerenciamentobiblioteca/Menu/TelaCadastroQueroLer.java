    private void preencherTabela() {
        try {
            List<QueroLer> lista = dao.listar();
            DefaultTableModel modelo = (DefaultTableModel) tabelaCadastro.getModel();
            modelo.setNumRows(0); // Limpa a tabela

            for (QueroLer l : lista) {
                modelo.addRow(new Object[]{
                    l.getId(),
                    l.getTitulo(),
                    l.getAutor(),
                    l.getGenero(),
                    l.getPrioridade(),});
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Erro ao listar: " + e.getMessage());
        }
    }

