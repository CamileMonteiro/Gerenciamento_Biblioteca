    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (modoEdicao) {
            JOptionPane.showMessageDialog(this,
                    "Voc√™ est√° no modo de edi√ß√£o. Use o bot√£o ATUALIZAR.");
            return;
        }

        if (txtID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um n√∫mero para o ID!");
            txtID.requestFocus();
            return;
        }

        try {
            QueroLer l = new QueroLer();

            l.setId(Integer.parseInt(txtID.getText()));

            l.setTitulo(txtTitulo.getText());
            l.setAutor(txtAutor.getText());
            l.setGenero(txtGenero.getText());

            if (cmbPrioridade.getSelectedItem() != null) {
                l.setPrioridade(cmbPrioridade.getSelectedItem().toString());
            } else {
                l.setPrioridade("");
            }

            dao.salvar(l);

            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
            limparCampos();
            preencherTabela();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O ID deve conter apenas n√∫meros!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar (verifique se o ID j√° existe): " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        try {
            QueroLer l = new QueroLer();
            l.setId(Integer.parseInt(txtID.getText()));
            l.setTitulo(txtTitulo.getText());
            l.setAutor(txtAutor.getText());
            l.setGenero(txtGenero.getText());
            l.setPrioridade(cmbPrioridade.getSelectedItem().toString());

            dao.atualizar(l);

            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");

            modoEdicao = false;
            btnCadastrar.setEnabled(true);
            limparCampos();
            preencherTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabelaCadastro.getSelectedRow();
        if (linha != -1) {
            int id = Integer.parseInt(tabelaCadastro.getValueAt(linha, 0).toString());

            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Tem certeza que deseja excluir o registro ID " + id + "?",
                    "Confirmar Exclus√£o",
                    JOptionPane.YES_NO_OPTION
            );
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    dao.excluir(id);
                    preencherTabela();
                    JOptionPane.showMessageDialog(this, "Exclu√≠do!");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

