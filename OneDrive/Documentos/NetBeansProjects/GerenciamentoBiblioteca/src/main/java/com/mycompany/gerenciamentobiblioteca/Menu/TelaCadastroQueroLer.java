    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tabelaCadastro.getSelectedRow();
        if (linha != -1) {

            modoEdicao = true;
            btnCadastrar.setEnabled(false);

            txtID.setText(tabelaCadastro.getValueAt(linha, 0).toString());
            txtTitulo.setText(tabelaCadastro.getValueAt(linha, 1).toString());
            txtAutor.setText(tabelaCadastro.getValueAt(linha, 2).toString());
            txtGenero.setText(tabelaCadastro.getValueAt(linha, 3).toString());
            cmbPrioridade.setSelectedItem(tabelaCadastro.getValueAt(linha, 4).toString());

            txtID.setEditable(false);

        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela para editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

